package JapaneseCars;

public abstract class JapaneseCars {

	private String name;
	private String color;
	private int doors;
	private int cylinders;
	
		

	public JapaneseCars(String name, String color) {
		super();
		this.name = name;
		this.color = color;
		this.doors = doors;
		this.cylinders = cylinders;
		
		
	}
	
	
}

